import { NextPage } from "next";
import React, { ReactNode, useEffect, useRef } from "react";
import Header from "@/components/Layout/Header";
import Head from "next/head";
import Footer from "@/components/Layout/Footer";
import moment from "moment";
import NavMobile from "@/components/Layout/NavMobile";

const Layout: NextPage<{
  children: ReactNode;
}> = ({ children }) => {
  const chatBoxRef = useRef<HTMLDivElement | null>(null);
  useEffect(() => {
    moment.locale("vi");
  }, []);

  useEffect(() => {
    chatBoxRef.current?.setAttribute("page_id", process.env.NEXT_PUBLIC_PAGE_ID as string);
    chatBoxRef.current?.setAttribute("attribution", "biz_inbox");
  }, []);

  return (
    <>
      <Head>
        <title>ChiChi - Shop</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="./favicon.png" />
      </Head>
      <NavMobile />
      <div className="absolute bottom-3 right-3 z-[100] shadow-md rounded-full w-16 h-1w-16 bg-white flex justify-center items-center">
        <div
          ref={chatBoxRef}
          id="fb-customer-chat"
          className="fb-customerchat"
        ></div>
      </div>
      <div className="relative bg-white w-screen h-screen overflow-y-scroll scrollbar-thin scrollbar-thumb-gray-400 scrollbar-track-gray-100 overflow-x-hidden">
        <Header />
        {children}
        <Footer />
      </div>
    </>
  );
};

export default Layout;
